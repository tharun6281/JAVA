public class unary
{
    public static void main(String[] args)
    {
        int a = 10;
        int b = 50;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a++);
        System.out.println(++a);
        System.out.println(a--);
        System.out.println(--a);
    }
}