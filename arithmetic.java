public class arithmetic
{
    public static void main(String[] args)
    {
        int a = 10;
        int b = 50;
        System.out.println(a + b);   
        System.out.println(a - b);  
        System.out.println(a * b);   
        System.out.println(a / b);  
        System.out.println(a % b);   
        System.out.println(a == b);  
    }
}
