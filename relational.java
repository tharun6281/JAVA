public class arithmetic
{
    public static void main(String[] args)
    {
        float a = 3.8f;
        float b = 5.9f;
        System.out.println(a!=b);
        System.out.println(a==b);
        System.out.println(a<b);
        System.out.println(a>b);
        System.out.println(a>=b);
        System.out.println(a<=b);
    }
}
